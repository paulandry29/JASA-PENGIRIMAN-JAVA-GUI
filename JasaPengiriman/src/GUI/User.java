    public void sum() {
        try {
            service Service = (service) Naming.lookup("rmi://localhost:5098/db");
            Integer total = Integer.parseInt(Service.sum_subTotal());
            //System.out.println("GUI.User.sum() = "+total);
            if (total == 0) {
                pembayaran_totalHarga.setText("0");
            } else {
                if (pembayaran_rbReguler.isSelected()) {
                    total = total + 15000;
                } else if (pembayaran_rbExpress.isSelected()) {
                    total = total + 25000;
                }
                String totali = total.toString();
                pembayaran_totalHarga.setText(totali);
            }

        } catch (Exception e) {
            System.out.println("GUI.User.sum() " + e.toString());
        }
    }

