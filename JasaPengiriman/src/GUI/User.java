    private void barang_btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_barang_btnSimpanActionPerformed
        try {
            String nama_barang = barang_namaBarang.getText();
            String jenis_barang = barang_cbJenisBarang.getSelectedItem().toString();
            int berat = (int) barang_beratBarang.getValue();
            int sub_total = berat * 3000; //harga per Kg

            if (nama_barang.isEmpty() || jenis_barang.isEmpty() || berat == 0) {
                JOptionPane.showMessageDialog(null, "Harap isi semua data", "ERROR", JOptionPane.ERROR_MESSAGE);
            } else {

                service Service = (service) Naming.lookup("rmi://localhost:5098/db");
                String result = Service.input_barang(nama_barang, jenis_barang, berat, sub_total);
                int last_id = Integer.parseInt(Service.lastInput_barang());
                Service.input_keranjang(nama_barang, last_id, jenis_barang, berat, sub_total);

                barang_namaBarang.setText("");
                barang_beratBarang.setValue(0);

                DefaultTableModel model = (DefaultTableModel) pembayaran_tblKeranjang.getModel();
                model.setRowCount(0);
                viewKeranjang();
                sum();

                JOptionPane.showMessageDialog(null, result, "Sukses", JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e, "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_barang_btnSimpanActionPerformed

